public class C3NR {
    private int num1;
    private int num2;
    private int num3;
    public C3NR(int num1, int num2, int num3)
    {
        this.num1=num1;
        this.num2=num2;
        this.num3=num3;
    }
    int getSum()
    {
        return num1+num2+num3;
    }

}
